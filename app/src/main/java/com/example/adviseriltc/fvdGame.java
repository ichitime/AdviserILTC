package com.example.adviseriltc;

public class fvdGame {
    String title;
    String tag1;
    String tag2;
    String tag3;
    String tag4;
    String price;
    String image;
    String link;
    String video;
    String release_date;
    String publisher;
    String description;
    String developer;
    String genre;

    public fvdGame() {
    }

    public fvdGame(String title, String tag1, String tag2, String tag3, String tag4, String price, String image, String link, String video, String release_date, String publisher, String description, String developer, String genre) {
        this.title = title;
        this.tag1 = tag1;
        this.tag2 = tag2;
        this.tag3 = tag3;
        this.tag4 = tag4;
        this.price = price;
        this.image = image;
        this.link = link;
        this.video = video;
        this.release_date = release_date;
        this.publisher = publisher;
        this.description = description;
        this.developer = developer;
        this.genre = genre;
    }
}
